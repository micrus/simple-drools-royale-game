package com.sample;

public class Statistic {
	int life;
	int atk;
	public Statistic(int life, int atk) {
		super();
		this.life = life;
		this.atk = atk;
	}
	public int getLife() {
		return life;
	}
	public void setLife(int life) {
		this.life = life;
	}
	public int getAtk() {
		return atk;
	}
	public void setAtk(int atk) {
		this.atk = atk;
	}
	
	
	
}

